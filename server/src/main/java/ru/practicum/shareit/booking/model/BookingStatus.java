package ru.practicum.shareit.booking.model;

/**
 * Перечисление статусов бронирования.
 * Определяет возможные состояния бронирования в системе.
 */
public enum BookingStatus {
    /**
     * Бронирование ожидает подтверждения от владельца ресурса.
     * Начальный статус после создания бронирования.
     */
    WAITING,

    /**
     * Бронирование подтверждено владельцем ресурса.
     * Пользователь может воспользоваться ресурсом в указанное время.
     */
    APPROVED,

    /**
     * Бронирование отклонено владельцем ресурса.
     * Пользователь не сможет воспользоваться ресурсом.
     */
    REJECTED,

    /**
     * Бронирование отменено пользователем (арендатором).
     * Ресурс снова доступен для других бронирований.
     */
    CANCELED
}

