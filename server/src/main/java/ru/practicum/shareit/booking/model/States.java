package ru.practicum.shareit.booking.model;

/**
 * Перечисление возможных состояний (фильтров) для выборки бронирований.
 */
public enum States {
    /**
     * Все бронирования без фильтрации по статусу или временному периоду.
     */
    ALL,

    /**
     * Текущие бронирования — те, что находятся в активном периоде:
     * - статус APPROVED;
     */
    CURRENT,

    /**
     * Прошедшие бронирования — завершённые по времени:
     * - статус APPROVED или REJECTED;
     */
    PAST,

    /**
     * Будущие бронирования — запланированные, но ещё не начавшиеся:
     * - статус APPROVED;
     */
    FUTURE,

    /**
     * Бронирования в статусе ожидания подтверждения:
     * - статус WAITING;
     */
    WAITING,

    /**
     * Отклонённые бронирования:
     * - статус REJECTED;
     */
    REJECTED
}
